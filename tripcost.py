def calculate_cost_of_trip(kilometers,milage,petrolprice):
    print('total cost of the trip is going to be :',(kilometers/milage)*petrolprice)
    
calculate_cost_of_trip(50,10,100)
#output:total cost of the trip is going to be : 500.0
calculate_cost_of_trip(220,20,110)
#output:total cost of the trip is going to be : 1210.0
